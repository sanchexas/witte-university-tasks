arr = [1, "hello", 23, [1,2,3]]

print(len(arr)) # длина массива
arr.append(1) # добавление элемента в конец
print(arr)
arr.remove(1) # удаляет только первый попавшийся элемент
print(arr)
numsArr = [2,1,4,8,5]
numsArr.sort() # сортирует массив чисел
print(numsArr)
numsArr.reverse() # сортирует по убыванию
print(numsArr)
arr.insert(0, "element") # вставляет элемент на определенную позицию
print(arr)